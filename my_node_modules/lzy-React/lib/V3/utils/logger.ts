//logger
class Logger {
    isOpen: boolean

    constructor() {
        this.isOpen = false
    }

    log(...args: unknown[]) {
        this.isOpen && console.log("[LZY-REACT-INFO]:", ...args)
    }

    error(...args: unknown[]) {
        console.error("[LZY-REACT-ERROR]:", ...args)
    }

    warn(...args: unknown[]) {
        console.warn("[LZY-REACT-WARN]:", ...args)
    }

    open() {
        this.isOpen = true
    }

    close() {
        this.isOpen = false
    }
}


const logger = new Logger()

export { logger }

